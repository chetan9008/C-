#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    cout << "floor of 2.6  = " << floor(2.6)<< endl;
    cout << "floor of 2.1  = " << ceil(2.1) << endl;
    cout << "floor of -2.1  = " << floor(-2.1) << endl;
    cout << "floor of -2.6  = " << ceil(-2.6) << endl;
    cout << "Absolute value of -2.1  = " << abs(-2.1) << endl;
    cout << "Absolute value of -2.1  = " << fabs(-2.1) << endl;
    cout << "log base 10 of 45  = " << log10(45) << endl;
}